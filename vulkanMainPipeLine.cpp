void app::initWindow() {
	glfwInit();

	const uint32_t width = 800;
	const uint32_t height = 600;
	std::string WindowName = "vulkan app";

	glfwWindowHint(GLFW_CLIENT_API, GLFW_NO_API);
	glfwWindowHint(GLFW_RESIZABLE, GLFW_FALSE);

	window = glfwCreateWindow(width, height, WindowName.c_str(), nullptr, nullptr);
	glfwSetWindowUserPointer(window, this);
	glfwSetFramebufferSizeCallback(window, framebufferResizeCallback);
}

void framebufferResizeCallback(GLFWwindow* window, int width, int height) {
	auto application = reinterpret_cast<app*>(glfwGetWindowUserPointer(window));
	application->set_framebufferResized(true);
}

void app::mainLoop() {
	while (!glfwWindowShouldClose(window)) {
		glfwPollEvents();
		drawFrame(window, device, graphicsQueue, presentQueue, swapChain, imageAvailableSemaphores, renderFinishedSemaphores, commandBuffers, currentFrame, inFlightFences, 
			imagesInFlight, swapChainImages, physicalDevice, surface, swapChainImageFormat, swapChainExtent, swapChainImageViews, renderPass,
			swapChainFramebuffers, commandPool, graphicsPipeline, pipelineLayout, vertexBuffer, indexBuffer, framebufferResized, descriptorSetLayout,
			uniformBuffers, uniformBuffersMemory, descriptorPool, descriptorSets, textureImageView, textureSampler, depthImageView, depthImage,
			depthImageMemory, indexArr);
	}
	vkDeviceWaitIdle(device);
}

void app::cleanUp() {

	cleanupSwapChain(device, swapChainFramebuffers, commandPool, commandBuffers,
		graphicsPipeline, pipelineLayout, renderPass, swapChainImageViews, swapChain,
		swapChainImages, uniformBuffers, uniformBuffersMemory, descriptorPool, 
		depthImage, depthImageMemory, depthImageView);

	vkDestroySampler(device, textureSampler, nullptr);
	
	vkDestroyImageView(device, textureImageView, nullptr);
	
	vkDestroyImage(device, textureImage, nullptr);
	vkFreeMemory(device, textureImageMemory, nullptr);
	
	vkDestroyDescriptorSetLayout(device, descriptorSetLayout, nullptr);

	vkDestroyBuffer(device, indexBuffer, nullptr);
	vkFreeMemory(device, indexBufferMemory, nullptr);

	vkDestroyBuffer(device, vertexBuffer, nullptr);
	vkFreeMemory(device, vertexBufferMemory, nullptr);

	for (size_t i = 0; i < MAX_FRAMES_IN_FLIGHT; i++) {
		vkDestroySemaphore(device, renderFinishedSemaphores[i], nullptr);
		vkDestroySemaphore(device, imageAvailableSemaphores[i], nullptr);
		vkDestroyFence(device, inFlightFences[i], nullptr);
	}

	vkDestroyCommandPool(device, commandPool, nullptr);

	vkDestroyDevice(device, nullptr);

	if (enableValidationLayers) {
		DestroyDebugUtilsMessengerEXT(instance, debugMessenger, nullptr);
	}
	vkDestroySurfaceKHR(instance, surface, nullptr);
	vkDestroyInstance(instance, nullptr);
	glfwDestroyWindow(window);
	glfwTerminate();
}

